/**
 * 获取中文字符的拼音首字母
 * 
 * @param {string} char - 中文字符
 * @returns {string} 拼音首字母（大写），如果不是中文或未收录则返回原字符
 */
export function getFirstLetter(char) {
  // 如果是英文字母，直接返回大写形式
  if (/[a-zA-Z]/.test(char)) {
    return char.toUpperCase();
  }
  
  // 如果是中文字符且在字典中有映射，返回对应的拼音首字母
  if (/[\u4e00-\u9fa5]/.test(char)) {
    // 如果在字典中有映射，返回对应的拼音首字母
    if (PINYIN_DICT[char]) {
      return PINYIN_DICT[char];
    }
    // 如果没有映射，直接返回该汉字
    return char;
  }
  
  // 对于其他字符，直接返回原字符
  return char;
}

